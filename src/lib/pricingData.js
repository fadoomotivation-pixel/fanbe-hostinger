/**
 * Get pricing for a specific project
 */
export const getProjectPricing = (projectId) => {
  return pricingData[projectId] || null;
};

/**
 * Calculate EMI for custom plot size
 */
export const calculateEMI = (projectId, plotSize) => {
  const project = pricingData[projectId];
  if (!project) return null;

  const totalCost = plotSize * project.ratePerSqYd;
  const bookingAmount = totalCost * (project.bookingPercentage / 100);
  const balanceAmount = totalCost - bookingAmount;
  const emi = balanceAmount / project.emiMonths;

  return {
    plotSize,
    ratePerSqYd: project.ratePerSqYd,
    totalCost: Math.round(totalCost),
    bookingAmount: Math.round(bookingAmount),
    balanceAmount: Math.round(balanceAmount),
    emi: Math.round(emi),
    emiMonths: project.emiMonths
  };
};

/**
 * Format currency in Indian format
 */
export const formatINR = (amount) => {
  return new Intl.NumberFormat('en-IN', {
    style: 'currency',
    currency: 'INR',
    maximumFractionDigits: 0
  }).format(amount);
};

/**
 * Format currency in Lakhs
 */
export const formatLakhs = (amount) => {
  const lakhs = amount / 100000;
  if (lakhs >= 1) {
    return `₹${lakhs.toFixed(2)}L`;
  }
  return formatINR(amount);
};
